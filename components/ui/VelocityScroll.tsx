import { useRef } from "react";
import {
  motion,
  useScroll,
  useSpring,
  useTransform,
  useMotionValue,
  useVelocity,
  useAnimationFrame,
} from "framer-motion";

interface VelocityScrollProps {
  text: string;
  default_velocity?: number;
  className?: string;
}

interface ParallaxProps {
  children: string;
  baseVelocity: number;
  className?: string;
}

function ParallaxText({ children, baseVelocity = 100, className }: ParallaxProps) {
  const baseX = useMotionValue(0);
  const { scrollY } = useScroll();
  const scrollVelocity = useVelocity(scrollY);
  const smoothVelocity = useSpring(scrollVelocity, {
    damping: 50,
    stiffness: 400,
  });
  const velocityFactor = useTransform(smoothVelocity, [0, 1000], [0, 5], {
    clamp: false,
  });

  const x = useTransform(baseX, (v) => `${v}%`);

  const directionFactor = useRef<number>(1);
  useAnimationFrame((t, delta) => {
    let moveBy = directionFactor.current * baseVelocity * (delta / 1000);

    if (velocityFactor.get() < 0) {
      directionFactor.current = -1;
    } else if (velocityFactor.get() > 0) {
      directionFactor.current = 1;
    }

    moveBy += directionFactor.current * moveBy * velocityFactor.get();

    baseX.set(baseX.get() + moveBy);
  });

  return (
    <div className="parallax overflow-hidden flex flex-nowrap whitespace-nowrap">
      <motion.div
        className={`scroller font-serif uppercase text-7xl md:text-9xl font-bold flex whitespace-nowrap ${className}`}
        style={{ x }}
      >
        <span className="block mr-12 md:mr-24">{children} </span>
        <span className="block mr-12 md:mr-24">{children} </span>
        <span className="block mr-12 md:mr-24">{children} </span>
        <span className="block mr-12 md:mr-24">{children} </span>
      </motion.div>
    </div>
  );
}

export const VelocityScroll = ({
  text,
  default_velocity = 5,
  className,
}: VelocityScrollProps) => {
  return (
    <section className="relative w-full overflow-hidden tracking-[-2px]">
      <ParallaxText baseVelocity={default_velocity} className={className}>
        {text}
      </ParallaxText>
      <ParallaxText baseVelocity={-default_velocity} className={className}>
        {text}
      </ParallaxText>
    </section>
  );
};
